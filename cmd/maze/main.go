package main

import (
	"fmt"
	"os"

	"github.com/necrophonic/gopher-maze/internal/game"
)

func main() {
	run()
}

func run() {
	g := game.New()

	if err := g.Run(); err != nil {
		fmt.Println("Error running game:", err)
		os.Exit(1)
	}
}

